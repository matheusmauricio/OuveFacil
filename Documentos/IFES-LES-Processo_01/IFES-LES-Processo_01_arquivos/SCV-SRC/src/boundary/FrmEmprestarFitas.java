    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
        this.emprestimoCorrente.setCliente(this.cliente);

        //Chamando o método da Controladora responsável por inserir um empréstimo
        if (SCV.inserirEmprestimo(this.emprestimoCorrente)) {
            //Cliente Inserido com Sucesso
            JOptionPane.showMessageDialog(this, "Empréstimo Inserido com Sucesso!");
            //Limpando o Formulário de Inserção
            this.dispose();
        } else {
            //Falha na Inserção do Cliente
            JOptionPane.showMessageDialog(this, "Falha na Inserção do Empréstimo!");
            //Limpando o Formulário de Inserção
            this.dispose();
        }
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

    private void jButtonConfirmarFilmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarFilmeActionPerformed
        this.jComboBoxFitas.removeAllItems();

        //Carregando jComboBoxFitas
        ArrayList<Fita> fitas = new ArrayList();
        Filme filme = new Filme();
        filme = (Filme) this.jComboBoxFilmes.getSelectedItem();
        fitas = SCV.selecionarFitasPorFilme(filme);
        for (Fita fita : fitas) {
            this.jComboBoxFitas.addItem(fita);
        }
    }//GEN-LAST:event_jButtonConfirmarFilmeActionPerformed
