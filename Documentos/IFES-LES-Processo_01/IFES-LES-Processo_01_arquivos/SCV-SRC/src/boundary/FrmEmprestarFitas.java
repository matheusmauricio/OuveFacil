    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        //Obtendo a fita selecionada
        Fita fita = new Fita();
        fita = (Fita) this.jComboBoxFitas.getSelectedItem();

        ItemDeEmprestimo itemDeEmprestimo = new ItemDeEmprestimo();
        itemDeEmprestimo.setFita(fita);

        this.emprestimoCorrente.adicionarItemDeEmprestimo(itemDeEmprestimo);

        //Adicionando a Fita no DefaultListModel
        listModel.addElement(fita);
        //Setando o DefaultListModel no jListFitas
        //Ele irá atualizar automaticamente o jListFitas
        jListFitas.setModel(listModel);

        //Obtendo o cliente selecionado
        this.cliente = (Cliente) this.jComboBoxClientes.getSelectedItem();
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

